def test_pickup():
    test1, test2, test3 = 'x', '*', '.' 
    
    item.clear()
    pickup(test1)
    assert test1 in item
    assert len(item) == 1
    assert pickup(test1) == 'ğŸª“'

    item.clear()
    pickup(test2)
    assert test2 in item
    assert len(item) == 1
    assert pickup(test2) == 'ğŸ”¥'

    item.clear()
    pickup(test3)
    assert test3 in item
    assert len(item) == 1
    assert pickup(test3) == 'ã€€'

